package source;

public class MileageDTO {
	private String milecode;
	private int nowmile;
	private String historydate;
	private String memcode;
	private String neayeong;

	public String getMilecode() {
		return milecode;
	}

	public void setMilecode(String milecode) {
		this.milecode = milecode;
	}

	public int getNowmile() {
		return nowmile;
	}

	public void setNowmile(int nowmile) {
		this.nowmile = nowmile;
	}

	public String getHistorydate() {
		return historydate;
	}

	public void setHistorydate(String historydate) {
		this.historydate = historydate;
	}

	public String getMemcode() {
		return memcode;
	}

	public void setMemcode(String memcode) {
		this.memcode = memcode;
	}

	public String getNeayeong() {
		return neayeong;
	}

	public void setNeayeong(String neayeong) {
		this.neayeong = neayeong;
	}

}
